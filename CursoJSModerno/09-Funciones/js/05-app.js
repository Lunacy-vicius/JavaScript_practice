// Parametros y argumentos en funciones 
function sumar(a, b){//a y b son parametros de funcion 
    console.log(a+b);

}

sumar(1, 3); //2 y 3 son los valores reales y se les conoce como argumentos 
sumar(200, 3);
sumar(300, 3);

function saludar(nombre, apellido){
    console.log(`Hola ${nombre} ${apellido}`)
}

saludar ("Ricardo, Sanchez");
